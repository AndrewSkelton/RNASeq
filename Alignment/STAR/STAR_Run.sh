##' Add Module
##'-----------------------------------------------------------------------------------------#
module add apps/STAR/2.5.2b
module add apps/samtools/1.3.1
##'-----------------------------------------------------------------------------------------#


##'Copy File to Scratch
##'-----------------------------------------------------------------------------------------#
cp ${1} ${TMPDIR}
cp -r ${3} ${TMPDIR}
##'-----------------------------------------------------------------------------------------#


##'Get Basenames
##'-----------------------------------------------------------------------------------------#
FQ_IN=$(basename "${1}")
STAR_IDX=$(basename "${3}")
##'-----------------------------------------------------------------------------------------#


##'Execute STAR 1 Pass
##'-----------------------------------------------------------------------------------------#
STAR --genomeDir ${TMPDIR}/${STAR_IDX} \
     --readFilesIn ${TMPDIR}/${FQ_IN} \
     --readFilesCommand zcat \
     --runThreadN 10 \
     --outFileNamePrefix ${TMPDIR}/${4}_ \
     --outSAMtype BAM Unsorted
##'-----------------------------------------------------------------------------------------#


##' Use samtools to coord sort the bam file, and index it
##'-----------------------------------------------------------------------------------------#
samtools sort ${TMPDIR}/${4}_Aligned.out.bam > ${TMPDIR}/${4}.bam
samtools index ${TMPDIR}/${4}.bam
##'-----------------------------------------------------------------------------------------#


##' Move output files from node scratch to lustre filesystem
##'-----------------------------------------------------------------------------------------#
mv ${TMPDIR}/${4}.* ${2}
